            <div class="modal-body">
                <p>In order to be able to add a fair amount of points to your segments, irrelevant of the Exam, you can roughly follow the guide below.</p>
                <p>The suggested way to create a consistent and fair way for professors to assign points to exam's segments is a scoring tool that lists the criteria for a particular assignment or task and describes different levels of performance for each criterion.</p>
                <p>By assigning point values to each level of performance, a clear and consistent way is given for professors to evaluate the subtask. <br>When creating the exam segment, you can assign a score for each criterion and then add up the points to arrive at a final score for the whole segment.</p>
                <h5>Criterion 1: Content Knowledge</h5>
                <ul>
                    <li><u>Exceeds Expectations</u>: Demonstrates comprehensive and accurate understanding of the content - <b>4 points</b></li>
                    <li><u>Meets Expectations</u>: Demonstrates basic understanding of the content - <b>3 points</b></li>
                    <li><u>Needs Improvement</u>: Demonstrates limited or inaccurate understanding of the content - <b>2 points</b></li>
                    <li><u>Unsatisfactory</u>: Demonstrates little or no understanding of the content - <b>1 point</b></li>
                </ul>
                <h5>Criterion 2: Clarity of Expression</h5>
                <ul>
                    <li><u>Exceeds Expectations</u>: Communicates ideas clearly and effectively, with excellent organization and flow - <b>3 points</b></li>
                    <li><u>Meets Expectations</u>: Communicates ideas clearly, with good organization and flow - <b>2 points</b></li>
                    <li><u>Needs Improvement</u>: Communicates ideas adequately, but with some organizational or flow issues - <b>1 point</b></li>
                    <li><u>Unsatisfactory</u>: Communication is unclear, disorganized, or difficult to follow - <b>0 points</b></li>
                </ul>
                <h5>Criterion 3: Use of Evidence</h5>
                <ul>
                    <li><u>Exceeds Expectations</u>: Supports arguments with strong, relevant evidence from multiple sources - <b>3 points</b></li>
                    <li><u>Meets Expectations</u>: Supports arguments with relevant evidence from one or two sources - <b>2 points</b></li>
                    <li><u>Needs Improvement</u>: Attempts to support arguments with evidence, but relevance or quality is questionable - <b>1 point</b></li>
                    <li><u>Unsatisfactory</u>: Fails to use evidence to support arguments - <b>0 points</b></li>
                </ul>
